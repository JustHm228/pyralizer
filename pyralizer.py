def get_caller():
    import inspect
    frm = inspect.stack()[2]
    mod = inspect.getmodule(frm[0])
    return mod

def destroy(debug: bool = False):
    mod = get_caller()
    scope = mod.__dict__
    keys = [ k for k in scope ]
    for k in scope['__builtins__'].__dict__:
        if k == 'print':
            pass
        else:
            scope[k] = None
    for k in keys:
        if debug and k == '__self__':
            scope[k] = mod
        elif k == '__builtins__':
            scope[k] = None
        else:
            del scope[k]
    print(scope)
